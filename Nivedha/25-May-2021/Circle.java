class Circle{
	public static void main(String args[]){
	 //area
	  {
	   double radius = 8.1;
	   double area = 3.14*radius*radius;
	   System.out.println("Area of Circle:"+area);
	  }

	  //circumference
	  {
		double radius = 6;
		double circumference = 2*3.14*radius;
		System.out.println("circumference of Circle:"+circumference);
	  }
	}
	
}